#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import yaml from 'js-yaml';

const rootDir = process.cwd();
const specsDir = path.join(rootDir, 'e2e/specs');
const onFilterDir = path.join(rootDir, 'e2e/generated/on-filter');
const oracleDir = path.join(rootDir, 'e2e/generated/oracle-workflows');
const workflowsDir = path.join(rootDir, '.github/workflows');

function listSpecFiles() {
  if (!fs.existsSync(specsDir)) {
    return [];
  }

  return fs
    .readdirSync(specsDir)
    .filter((entry) => entry.endsWith('.yml') || entry.endsWith('.yaml'))
    .sort();
}

function assertValidSpec(specPath, content) {
  if (!content || typeof content !== 'object') {
    throw new Error(`Invalid spec file: ${specPath}`);
  }

  if (typeof content.name !== 'string' || content.name.trim() === '') {
    throw new Error(`Missing/invalid "name" in ${specPath}`);
  }

  if (!content.on || typeof content.on !== 'object') {
    throw new Error(`Missing/invalid "on" block in ${specPath}`);
  }
}

function serializeYaml(doc) {
  return yaml.dump(doc, {
    lineWidth: -1,
    noRefs: true,
    sortKeys: false,
  });
}

function toOnFilterYaml(onBlock) {
  return serializeYaml(onBlock);
}

function indentBlock(content, spaces) {
  const prefix = ' '.repeat(spaces);
  return content
    .trimEnd()
    .split('\n')
    .map((line) => `${prefix}${line}`)
    .join('\n');
}

function toOracleWorkflowYaml(name, onBlock) {
  const onYaml = toOnFilterYaml(onBlock);

  return [
    `name: oracle-${name}`,
    'on:',
    indentBlock(onYaml, 2),
    'jobs:',
    '  oracle:',
    '    runs-on: ubuntu-slim',
    '    steps:',
    '      - run: echo oracle workflow trigger definition',
    '',
  ].join('\n');
}

function buildVerifyWorkflowYaml(specs) {
  const lines = [];

  lines.push('# This file is auto-generated by scripts/generate-e2e-artifacts.mjs');
  lines.push('# Do not edit manually. Run `npm run e2e:generate` to regenerate.');
  lines.push('name: verify');
  lines.push("on:");
  lines.push("  push:");
  lines.push("    branches: ['**']");
  lines.push("    tags: ['**']");
  lines.push("  pull_request:");
  lines.push("    branches: ['**']");
  lines.push("");
  lines.push("permissions:");
  lines.push("  actions: read");
  lines.push("  contents: read");
  lines.push("");
  lines.push("jobs:");
  lines.push("  verify:");
  lines.push("    runs-on: ubuntu-slim");
  lines.push("    steps:");
  lines.push("      - uses: actions/checkout@v4");
  lines.push("        with:");
  lines.push("          fetch-depth: 0");

  for (const spec of specs) {
    const id = spec.name;
    lines.push('');
    lines.push(`      - name: Load ${id} filter`);
    lines.push(`        id: load-${id}`);
    lines.push("        run: |");
    lines.push(`          { echo 'yaml<<EOF'; cat e2e/generated/on-filter/${id}.yml; echo 'EOF'; } >> "$GITHUB_OUTPUT"`);
    lines.push(`      - name: Run on-filter ${id}`);
    lines.push("        uses: ./");
    lines.push(`        id: on-filter-${id}`);
    lines.push("        with:");
    lines.push(`          on: \${{ steps.load-${id}.outputs.yaml }}`);
  }

  lines.push('');
  lines.push('      - name: Wait for oracle workflows to be enqueued');
  lines.push('        run: sleep 60');
  lines.push('');
  lines.push('      - name: Compare on-filter results with oracle workflows');
  lines.push('        uses: actions/github-script@v7');
  lines.push('        with:');
  lines.push('          script: |');

  const scriptLines = buildCompareScript(specs);
  for (const sl of scriptLines) {
    lines.push(`            ${sl}`);
  }

  lines.push('');
  return lines.join('\n') + '\n';
}

function buildCompareScript(specs) {
  const lines = [];
  lines.push('// For pull_request events, context.sha is the merge commit SHA,');
  lines.push('// but workflow runs use the PR head commit SHA as head_sha.');
  lines.push("const sha = context.eventName === 'pull_request'");
  lines.push('  ? context.payload.pull_request.head.sha');
  lines.push('  : context.sha;');
  lines.push('const { data } = await github.rest.actions.listWorkflowRunsForRepo({');
  lines.push('  owner: context.repo.owner,');
  lines.push('  repo: context.repo.repo,');
  lines.push('  head_sha: sha,');
  lines.push('  per_page: 100,');
  lines.push('});');
  lines.push('const oracleRuns = new Set(');
  lines.push("  data.workflow_runs");
  lines.push("    .filter(r => r.name.startsWith('oracle-'))");
  lines.push("    .map(r => r.name.replace('oracle-', ''))");
  lines.push(');');
  lines.push('const results = {');
  for (const spec of specs) {
    lines.push(`  '${spec.name}': '\${{ steps.on-filter-${spec.name}.outputs.result }}',`);
  }
  lines.push('};');
  lines.push('let failures = 0;');
  lines.push('for (const [name, result] of Object.entries(results)) {');
  lines.push("  const oracleRan = oracleRuns.has(name);");
  lines.push("  const expected = result === 'true';");
  lines.push('  if (expected !== oracleRan) {');
  lines.push('    core.error(`MISMATCH: ${name} on-filter=${result} oracle=${oracleRan}`);');
  lines.push('    failures++;');
  lines.push('  } else {');
  lines.push('    core.info(`OK: ${name} on-filter=${result} oracle=${oracleRan}`);');
  lines.push('  }');
  lines.push('}');
  lines.push("if (failures > 0) core.setFailed(`${failures} mismatch(es)`);");
  return lines;
}

function buildTriggerWorkflowYaml(specs) {
  const lines = [];

  lines.push('# This file is auto-generated by scripts/generate-e2e-artifacts.mjs');
  lines.push('# Do not edit manually. Run `npm run e2e:generate` to regenerate.');
  lines.push('name: trigger-e2e');
  lines.push("on:");
  lines.push("  workflow_dispatch:");
  lines.push("");
  lines.push("jobs:");
  lines.push("  trigger:");
  lines.push("    runs-on: ubuntu-slim");
  lines.push("    steps:");
  lines.push("      - uses: actions/checkout@v4");
  lines.push("        with:");
  lines.push("          fetch-depth: 0");
  lines.push("");
  lines.push("      - name: Create GitHub App Token");
  lines.push("        uses: actions/create-github-app-token@v1");
  lines.push("        id: app-token");
  lines.push("        with:");
  lines.push("          app-id: ${{ secrets.E2E_APP_ID }}");
  lines.push("          private-key: ${{ secrets.E2E_APP_PRIVATE_KEY }}");
  lines.push("");
  lines.push("      - name: Setup git");
  lines.push("        run: |");
  lines.push('          git config user.email "e2e-bot@example.com"');
  lines.push('          git config user.name "E2E Bot"');
  lines.push('          REMOTE="https://x-access-token:${{ steps.app-token.outputs.token }}@github.com/${{ github.repository }}.git"');
  lines.push('          git remote set-url origin "$REMOTE"');

  // Determine which triggers are needed based on specs
  const needsPushMain = specs.some((s) => hasPushBranches(s.on));
  const needsPushFeature = specs.some((s) => hasPushBranchesIgnore(s.on));
  const needsTagV = specs.some((s) => hasPushTags(s.on));
  const needsTagRc = specs.some((s) => hasPushTagsIgnore(s.on));
  const needsPathsSrc = specs.some((s) => hasPushPaths(s.on));
  const needsPathsDocs = specs.some((s) => hasPushPathsIgnore(s.on));
  const needsPrOpened = specs.some((s) => hasPullRequest(s.on));
  const needsPrSync = specs.some((s) => hasPrTypesSync(s.on));

  if (needsPushMain) {
    lines.push('');
    lines.push('      - name: "Trigger: push to main"');
    lines.push('        run: |');
    lines.push('          git commit --allow-empty -m "e2e: push-main (${{ github.run_id }})"');
    lines.push('          git push origin HEAD:main');
  }

  if (needsPushFeature) {
    lines.push('');
    lines.push('      - name: "Trigger: push to feature branch"');
    lines.push('        run: |');
    lines.push('          BRANCH="e2e/test-${{ github.run_id }}"');
    lines.push('          git checkout -b "$BRANCH"');
    lines.push('          git commit --allow-empty -m "e2e: push-feature (${{ github.run_id }})"');
    lines.push('          git push -u origin "$BRANCH"');
    lines.push('          git checkout main');
  }

  if (needsTagV) {
    lines.push('');
    lines.push('      - name: "Trigger: push tag v*"');
    lines.push('        run: |');
    lines.push('          TAG="v0.0.0-e2e-${{ github.run_id }}"');
    lines.push('          git tag "$TAG"');
    lines.push('          git push origin "$TAG"');
  }

  if (needsTagRc) {
    lines.push('');
    lines.push('      - name: "Trigger: push tag *-rc*"');
    lines.push('        run: |');
    lines.push('          TAG="0.0.0-rc-e2e-${{ github.run_id }}"');
    lines.push('          git tag "$TAG"');
    lines.push('          git push origin "$TAG"');
  }

  if (needsPathsSrc) {
    lines.push('');
    lines.push('      - name: "Trigger: push with src/ changes"');
    lines.push('        run: |');
    lines.push('          BRANCH="e2e/paths-src-${{ github.run_id }}"');
    lines.push('          git checkout -b "$BRANCH"');
    lines.push('          echo "// e2e test ${{ github.run_id }}" > src/.e2e-test-file');
    lines.push('          git add src/.e2e-test-file');
    lines.push('          git commit -m "e2e: paths-src (${{ github.run_id }})"');
    lines.push('          git push -u origin "$BRANCH"');
    lines.push('          git checkout main');
  }

  if (needsPathsDocs) {
    lines.push('');
    lines.push('      - name: "Trigger: push with docs/ only changes"');
    lines.push('        run: |');
    lines.push('          BRANCH="e2e/paths-docs-${{ github.run_id }}"');
    lines.push('          git checkout -b "$BRANCH"');
    lines.push('          mkdir -p docs');
    lines.push('          echo "# e2e test ${{ github.run_id }}" > docs/.e2e-test-file.md');
    lines.push('          git add docs/.e2e-test-file.md');
    lines.push('          git commit -m "e2e: paths-docs (${{ github.run_id }})"');
    lines.push('          git push -u origin "$BRANCH"');
    lines.push('          git checkout main');
  }

  if (needsPrOpened) {
    lines.push('');
    lines.push('      - name: "Trigger: create PR (opened)"');
    lines.push('        env:');
    lines.push('          GH_TOKEN: ${{ steps.app-token.outputs.token }}');
    lines.push('        run: |');
    lines.push('          BRANCH="e2e/pr-${{ github.run_id }}"');
    lines.push('          git checkout -b "$BRANCH"');
    lines.push('          git commit --allow-empty -m "e2e: pr-opened (${{ github.run_id }})"');
    lines.push('          git push -u origin "$BRANCH"');
    lines.push('          gh pr create --base main --head "$BRANCH" \\');
    lines.push('            --title "E2E test: ${{ github.run_id }}" \\');
    lines.push('            --body "Auto-generated E2E test PR"');
  }

  if (needsPrSync) {
    lines.push('');
    lines.push('      - name: "Trigger: push to PR (synchronize)"');
    lines.push('        run: |');
    lines.push('          BRANCH="e2e/pr-${{ github.run_id }}"');
    lines.push('          git checkout "$BRANCH"');
    lines.push('          git commit --allow-empty -m "e2e: pr-sync (${{ github.run_id }})"');
    lines.push('          git push origin "$BRANCH"');
    lines.push('          git checkout main');
  }

  // Cleanup
  lines.push('');
  lines.push('      - name: Cleanup');
  lines.push('        if: always()');
  lines.push('        env:');
  lines.push('          GH_TOKEN: ${{ steps.app-token.outputs.token }}');
  lines.push('        run: |');

  if (needsPrOpened) {
    lines.push('          gh pr close "e2e/pr-${{ github.run_id }}" --delete-branch || true');
  }
  if (needsPushFeature) {
    lines.push('          git push origin --delete "e2e/test-${{ github.run_id }}" || true');
  }
  if (needsPathsSrc) {
    lines.push('          git push origin --delete "e2e/paths-src-${{ github.run_id }}" || true');
  }
  if (needsPathsDocs) {
    lines.push('          git push origin --delete "e2e/paths-docs-${{ github.run_id }}" || true');
  }
  if (needsTagV) {
    lines.push('          git push origin --delete "v0.0.0-e2e-${{ github.run_id }}" || true');
  }
  if (needsTagRc) {
    lines.push('          git push origin --delete "0.0.0-rc-e2e-${{ github.run_id }}" || true');
  }

  lines.push('');
  return lines.join('\n') + '\n';
}

// Helper functions to detect which triggers specs require
function hasPushBranches(onBlock) {
  return onBlock.push && (onBlock.push.branches || (!onBlock.push.tags && !onBlock.push['tags-ignore']));
}

function hasPushBranchesIgnore(onBlock) {
  return onBlock.push && onBlock.push['branches-ignore'];
}

function hasPushTags(onBlock) {
  return onBlock.push && onBlock.push.tags;
}

function hasPushTagsIgnore(onBlock) {
  return onBlock.push && onBlock.push['tags-ignore'];
}

function hasPushPaths(onBlock) {
  return onBlock.push && onBlock.push.paths;
}

function hasPushPathsIgnore(onBlock) {
  return onBlock.push && onBlock.push['paths-ignore'];
}

function hasPullRequest(onBlock) {
  return !!onBlock.pull_request;
}

function hasPrTypesSync(onBlock) {
  return (
    onBlock.pull_request &&
    onBlock.pull_request.types &&
    onBlock.pull_request.types.includes('synchronize')
  );
}

function cleanDir(dirPath, keepFiles) {
  if (!fs.existsSync(dirPath)) {
    fs.mkdirSync(dirPath, { recursive: true });
    return;
  }

  const keep = new Set(keepFiles);
  for (const entry of fs.readdirSync(dirPath)) {
    const filePath = path.join(dirPath, entry);
    if (fs.statSync(filePath).isFile() && !keep.has(entry)) {
      fs.unlinkSync(filePath);
    }
  }
}

function cleanWorkflowFiles(keepOracleFiles, extraKeep) {
  const keep = new Set([...keepOracleFiles, ...extraKeep]);
  if (!fs.existsSync(workflowsDir)) {
    return;
  }
  for (const entry of fs.readdirSync(workflowsDir)) {
    if (!entry.startsWith('oracle-')) continue;
    const filePath = path.join(workflowsDir, entry);
    if (fs.statSync(filePath).isFile() && !keep.has(entry)) {
      fs.unlinkSync(filePath);
    }
  }
}

function writeGeneratedFiles() {
  const specFiles = listSpecFiles();
  const onFilterFiles = [];
  const oracleFiles = [];
  const oracleWorkflowFiles = [];
  const specs = [];

  for (const specFile of specFiles) {
    const specPath = path.join(specsDir, specFile);
    const loaded = yaml.load(fs.readFileSync(specPath, 'utf8'));
    assertValidSpec(specPath, loaded);

    const spec = loaded;
    const outputName = `${spec.name}.yml`;

    const onFilterYaml = toOnFilterYaml(spec.on);
    const oracleWorkflowYaml = toOracleWorkflowYaml(spec.name, spec.on);

    fs.mkdirSync(onFilterDir, { recursive: true });
    fs.mkdirSync(oracleDir, { recursive: true });
    fs.mkdirSync(workflowsDir, { recursive: true });

    fs.writeFileSync(path.join(onFilterDir, outputName), onFilterYaml);
    fs.writeFileSync(path.join(oracleDir, outputName), oracleWorkflowYaml);

    // Also write oracle workflow to .github/workflows/
    const oracleWfName = `oracle-${spec.name}.yml`;
    fs.writeFileSync(path.join(workflowsDir, oracleWfName), oracleWorkflowYaml);

    onFilterFiles.push(outputName);
    oracleFiles.push(outputName);
    oracleWorkflowFiles.push(oracleWfName);
    specs.push(spec);
  }

  // Generate verify.yml
  const verifyYaml = buildVerifyWorkflowYaml(specs);
  fs.writeFileSync(path.join(workflowsDir, 'verify.yml'), verifyYaml);

  // Generate trigger-e2e.yml
  const triggerYaml = buildTriggerWorkflowYaml(specs);
  fs.writeFileSync(path.join(workflowsDir, 'trigger-e2e.yml'), triggerYaml);

  cleanDir(onFilterDir, onFilterFiles);
  cleanDir(oracleDir, oracleFiles);
  cleanWorkflowFiles(oracleWorkflowFiles, []);

  console.log(`Generated ${onFilterFiles.length} spec artifact(s).`);
  console.log(`Generated verify.yml, trigger-e2e.yml, and ${oracleWorkflowFiles.length} oracle workflow(s) in .github/workflows/.`);
}

writeGeneratedFiles();
